var roundUp = function (num, precision) {
    precision = Math.pow(10, precision);
    return Math.ceil(num * precision) / precision;
};
var calculate = function () {
    var sensorWidth = document.getElementById("sensorWidth").valueAsNumber;
    var focalLength = document.getElementById("focalLength").valueAsNumber;
    var flightHeight = document.getElementById("flightHeight").valueAsNumber;
    var imageWidth = document.getElementById("imageWidth").valueAsNumber;
    var imageHeight = document.getElementById("imageHeight").valueAsNumber;
    var gsd = document.getElementById("gsd").valueAsNumber;
    var siteWidth = document.getElementById("siteWidth").valueAsNumber;
    var siteHeight = document.getElementById("siteHeight").valueAsNumber;
    var speed = document.getElementById("speed").valueAsNumber;
    var gsdCalcd = (sensorWidth * flightHeight * 100) / (focalLength * imageWidth);
    document.getElementById("gsd").value = (gsdCalcd).toFixed(2); //tofixed rounds it to specified no & returns STRING 
    var dwCalcd = imageWidth * gsd / 100;
    document.getElementById("dw").value = (dwCalcd).toFixed(2);
    var dhCalcd = imageHeight * gsd / 100;
    document.getElementById("dh").value = (dhCalcd).toFixed(2);
    var NoOfPhotosTBTakenAlongWidthCalcd = siteWidth / (dhCalcd * 80 / 100);
    document.getElementById("noOfPhotosAlongWidth").valueAsNumber = roundUp((NoOfPhotosTBTakenAlongWidthCalcd), 0);
    var NoOfPhotosTBTakenAlongHeightCalcd = siteHeight / (dwCalcd * 80 / 100);
    document.getElementById("noOfPhotosAlongHeight").valueAsNumber = roundUp((NoOfPhotosTBTakenAlongHeightCalcd), 0);
    var TotalPhotosTBTakenAlongWidthCalcd = (roundUp((NoOfPhotosTBTakenAlongWidthCalcd), 0) * siteHeight) + siteWidth;
    document.getElementById("totalPhotosAlongWidth").valueAsNumber = (TotalPhotosTBTakenAlongWidthCalcd);
    var TotalPhotosTBTakenAlongHeightCalcd = (roundUp((NoOfPhotosTBTakenAlongHeightCalcd), 0) * siteWidth) + siteHeight;
    document.getElementById("totalPhotosAlongHeight").valueAsNumber = (TotalPhotosTBTakenAlongHeightCalcd);
    var autoClickIntervalAlongWidthCalcd = siteWidth / (speed * 1000 / 3600) / roundUp((NoOfPhotosTBTakenAlongWidthCalcd), 0);
    document.getElementById("autoClickIntervalAlongWidth").value = (autoClickIntervalAlongWidthCalcd).toFixed(2);
    var autoClickIntervalAlongHeightCalcd = siteHeight / (speed * 1000 / 3600) / roundUp((NoOfPhotosTBTakenAlongHeightCalcd), 0);
    document.getElementById("autoClickIntervalAlongHeight").value = (autoClickIntervalAlongHeightCalcd).toFixed(2);
    var totalDistanceCalcd = (siteWidth * roundUp((NoOfPhotosTBTakenAlongHeightCalcd), 0)) + siteHeight;
    document.getElementById("totalDistance").value = (totalDistanceCalcd).toFixed(2);
    var totalTimeCalcd = (totalDistanceCalcd / (speed * 1000 / 3600) / 60) + flightHeight / (speed * 1000 / 3600) / 60;
    document.getElementById("totalTime").value = (totalTimeCalcd).toFixed(2);
    var rthDistanceCalcd = siteWidth + siteHeight + dhCalcd + ((roundUp((NoOfPhotosTBTakenAlongHeightCalcd), 0) - 1) * (siteWidth - dhCalcd));
    document.getElementById("rthDistance").value = (rthDistanceCalcd).toFixed(2);
    var rthTimeCalcd = rthDistanceCalcd / (speed * 1000 / 3600) / 60;
    document.getElementById("rthTime").value = (rthTimeCalcd).toFixed(2);
};
